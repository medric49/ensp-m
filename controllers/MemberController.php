    }

    public function actionDetailSession($q = 0) {
        MemberSessionManager::setHome("sessions");
        if ($q) {
            $session = Session::findOne($q);
            if ($session) {
                return $this->render("detailsession",compact('session'));
            }
            else
                return RedirectionManager::abort($this);
        }
        else
            return RedirectionManager::abort($this);
     }
}