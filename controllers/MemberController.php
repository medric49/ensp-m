                $this->view->params = ['user'=> $this->user,'member'=> $this->member];
                return $this->render('profil',['member'=> $member, 'user'=> $user]);
            
        
    }

    public function actionProfilMembre($m=0, $n=0) {
        MemberSessionManager::setMembers();
       if($m){
